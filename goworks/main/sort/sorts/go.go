package sorts
import(
	"sort"
)

type inta []int
func (f inta) Len() int           { return len(f) }
func (f inta) Less(i, j int) bool { return f[i] < f[j] }
func (f inta) Swap(i, j int)      { f[i], f[j] = f[j], f[i] }

func Dsort(data []int){
	sort.Sort(inta(data))
}
